<script lang="ts" setup>
import axios from "axios";
import { loadAssets } from "../utils/loadAssets";
import { useRouter } from "vue-router";
import { onBeforeUnmount, ref } from "vue";
import img from "/assets/user_ico.webp";
let controller: AbortController | null = null;
import "/assets/loading_bg.webp";
import { showToast } from "vant";
const router = useRouter();
const time = ref(0);
let sseClint: EventSource;
init();
onBeforeUnmount(() => {
  sseClint?.close();
});
async function init() {
  try {
    const user = localStorage.getItem("user");
    const { data } = await axios.get(`/api/gameInfo?user=${user}`);
    if (data) {
      toGame();
    } else {
      start();
    }
  } catch (error) {}
}
async function start() {
  setInterval(() => {
    time.value += 1;
  }, 1000);
  controller = new AbortController();

  const tag = await new Promise((res) => {
    const sse = new EventSource(`/sse/pending`);
    sseClint = sse;
    sse.onmessage = (event) => {
      const data = event.data;
      sse.close();
      res(data);
    };
    sse.onerror = (event: any) => {
      const msg = event.data as string;
      if (msg) {
        showToast(msg);
      } else {
        showToast("游戏出错，重进试试");
      }
      res(0);
      sse.close();
    };
  });
  if (Number(tag) === 1) {
    toGame();
  } else {
    cancel();
  }
}
function cancel() {
  axios.get("/api/cancel");
  router.replace(`/`);
}

async function toGame() {
  router.replace("/load");
}

function formatTime(seconds: number) {
  const minutes = Math.floor(seconds / 60);
  const remainingSeconds = seconds % 60;
  const formattedMinutes = String(minutes).padStart(2, "0");
  const formattedSeconds = String(remainingSeconds).padStart(2, "0");
  return `${formattedMinutes}:${formattedSeconds}`;
}
